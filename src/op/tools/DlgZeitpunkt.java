    /**
     * Zeigt das Datumsfenster an.
     * @param datum
     * @param min
     * @param max
     * @param withTime. Soll auch nach der Uhrzeit gefragt werden ?
     * @return das gew‰hlte Datum. NULL, wenn abgebrochen.
     */
    public Date showDialog(Date datum, Date min, Date max, boolean withTime) {
        refDate = datum;
        minDate = min;
        maxDate = max;
        jcKalender.setMinSelectableDate(min);
        jcKalender.setMaxSelectableDate(max);

        spinMinute.setVisible(withTime);
        spinStunde.setVisible(withTime);
        lbl1.setVisible(withTime);
        lbl2.setVisible(withTime);
        this.withTime = withTime;

        if (withTime) {
            ignore = true;
            spinStunde.setModel(new SpinnerNumberModel(0, 0, 23, 1));
            spinMinute.setModel(new SpinnerNumberModel(0, 0, 59, 1));
            GregorianCalendar gc = SYSCalendar.toGC(datum);
            spinStunde.setValue(gc.get(GregorianCalendar.HOUR_OF_DAY));
            spinMinute.setValue(gc.get(GregorianCalendar.MINUTE));
            // Die folgenden beiden Zeilen legen eine zweistellige Darstellung beim Spinner fest.
            spinStunde.setEditor(new JSpinner.NumberEditor(spinStunde, "00"));
            spinMinute.setEditor(new JSpinner.NumberEditor(spinMinute, "00"));
            ignore = false;
        }

        jcKalender.setDate(datum);
        DateFormat df = DateFormat.getDateInstance();
        txtDatum.setText(df.format(datum));
        setDateLabel(datum);
        SYSTools.centerOnParent(parent, this);
        setVisible(true);
        Date selectedDate = null;
        if (!cancel) {
            selectedDate = getTime();
        }
        return selectedDate;
    }

    private Date getTime() {
        GregorianCalendar result = SYSCalendar.erkenneDatum(txtDatum.getText());
        if (result.equals(SYSConst.BIS_AUF_WEITERES_WO_TIME)) {
            result = SYSConst.BIS_AUF_WEITERES;
        } else {
            if (withTime) {
                String zeit = spinStunde.getValue() + ":" + spinMinute.getValue();
                result = SYSCalendar.erkenneUhrzeit(zeit, result);
            } else {
                result = SYSCalendar.trimTime(result);
            }
        }
        return new Date(result.getTimeInMillis());
    }

