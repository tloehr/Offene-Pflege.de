    private void setDateLabel(Date date) {
        //DateFormat df = DateFormat.getDateTimeInstance(DateFormat.LONG, DateFormat.LONG);
        String text = "";
        if (date.equals(SYSConst.DATE_BIS_AUF_WEITERES)) {
            text = "bis auf weiteres";
        } else if (date.equals(SYSConst.DATE_VON_ANFANG_AN)) {
            text = "von Anfang an";
        } else {
            String format = "EEEE', den 'dd'. 'MMMM' 'yyyy";
            if (withTime) {
                format += "' 'HH':'mm' Uhr'";
            }
            SimpleDateFormat sdf = new SimpleDateFormat(format);
            text = sdf.format(date);
        }
        lblDatum.setText(text);
    }

    private void txtDatumFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDatumFocusLost
        GregorianCalendar gc;
        try {
            gc = SYSCalendar.erkenneDatum(((JTextField) evt.getSource()).getText());
        } catch (NumberFormatException ex) {
            gc = SYSCalendar.toGC(refDate);
        }
        // Datum auﬂerhalb des erlaubten Intervals ?
        if (!SYSCalendar.betweenOverlap(minDate, maxDate, new Date(gc.getTimeInMillis()))) {
            gc = SYSCalendar.toGC(refDate);
        }
        ((JTextField) evt.getSource()).setText(SYSCalendar.printGCGermanStyle(gc));
        jcKalender.setDate(new Date(gc.getTimeInMillis()));
        setDateLabel(new Date(gc.getTimeInMillis()));
        if (withTime) {
            ignore = true;
            spinStunde.setValue(gc.get(GregorianCalendar.HOUR_OF_DAY));
            spinMinute.setValue(gc.get(GregorianCalendar.MINUTE));
            ignore = false;
        }
    }//GEN-LAST:event_txtDatumFocusLost

